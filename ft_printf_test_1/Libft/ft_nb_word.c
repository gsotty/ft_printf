int		ft_nb_word(const char *s, char c)
{
	int		i;
	int		k;

	i = -1;
	k = 0;
	while (s[++i])
	{
		if (s[i] != c)
		{
			i++;
			while (s[i] != c && s[i] != '\0')
				i++;
			k++;
			i--;
		}
	}
	return (k);
}
